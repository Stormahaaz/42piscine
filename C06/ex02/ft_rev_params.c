#include <unistd.h>

int		main(int ac, char **av)
{
	int	i;

	i = ac;
	if (ac != 1)
		while (i > 1)
		{
			while (*av[i - 1])
				write(1, av[i - 1]++, 1);
			write(1, "\n", 1);
			i--;
		}
	return (0);
}
